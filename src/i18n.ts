const dictionaries: Record<AppLanguage, Dict> = { zh, en, ja, ko };

export function normalizeLanguage(value: unknown): AppLanguage {
  return value === "en" || value === "ja" || value === "ko" || value === "zh" ? value : "zh";
}

export function makeTranslator(language: AppLanguage): Translator {
  const dict = dictionaries[language] ?? zh;
  return (key) => dict[key] ?? zh[key] ?? key;
}
